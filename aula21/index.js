/*
* Operadores de comparação
* > maior que
* < menor que
* >= maior ou igual
* <= menor ou igual
* == igual
* === estritamente igual
* != diferente
* !== estritamente diferente
*/

console.log(10 > 5);
console.log(10 < 5);
console.log(10 >= 5);
console.log(10 <= 5);
console.log(10 == 5);
console.log(10 === 5);
console.log(10 != 5);
console.log(10 !== 5);

